class Data_Types1
{
public static void main(String[] args)
{
byte age=50;
byte exp=20;
short daily_salary=30000;
int monthly_salary=daily_salary*30;
long funds=67676767676767L;
float increase=5.29f;
double acc_increase=5.29752d;
System.out.println("Employee age:"+age);
System.out.println("Experience:"+exp);
System.out.println("Salary per day:"+daily_salary);
System.out.println("Salary per month:"+monthly_salary);
System.out.println("Company's funds:"+funds);
System.out.println("Increase percentage of salary after every year:"+increase);
System.out.println("Accurate increase percentage of salary:"+acc_increase);
}
}